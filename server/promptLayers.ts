// 조립기 — 계층을 합쳐 캐시 가능한 고정 블록(들)을 만든다
// ─────────────────────────────────────────────────────────────

export type ConsultMode = "personal" | "compatibility";

export type SystemLayers = {
  /** 고정 캐시 블록(들). 기본 1개([L1+L2+L3]). splitStyleLayer=true면 [L1+L2],[L3]로 2개. */
  cachedBlocks: string[];
  /** 계층 버전 메타데이터 — 로깅/추적용 */
  versions: typeof LAYER_VERSIONS;
};

/**
 * 모드에 맞는 L1+L2+L3 고정 계층을 조립한다.
 * @param mode 상담 모드(personal | compatibility)
 * @param opts.splitStyleLayer L3(스타일)를 별도 캐시 블록으로 분리할지 여부.
 *        A/B로 스타일을 자주 바꿀 때만 true 권장(토큰이 캐시 최소단위를 넘을 때 의미 있음).
 */
export function buildSystemLayers(
  mode: ConsultMode,
  opts: { splitStyleLayer?: boolean } = {},
): SystemLayers {
  const l1 = L1_CORE_PERSONA;
  const ganji = buildGanjiMappingTable();
  const l2Policy = mode === "personal" ? L2_PERSONAL_POLICY : L2_COMPATIBILITY_POLICY;
  const l2 = `${ganji}\n${l2Policy}`;
  const l3 = mode === "personal" ? L3_PERSONAL_STYLE : L3_COMPATIBILITY_STYLE;

  if (opts.splitStyleLayer) {
    // 2단 캐시: [L1+L2] (거의 불변) / [L3] (자주 변경)
    return {
      cachedBlocks: [`${l1}\n\n${l2}`, l3],
      versions: LAYER_VERSIONS,
    };
  }

  // 기본: 단일 고정 캐시 블록 [L1+L2+L3]
  return {
    cachedBlocks: [`${l1}\n\n${l2}\n${l3}`],
    versions: LAYER_VERSIONS,
  };
}

