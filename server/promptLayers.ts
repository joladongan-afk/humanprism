/**
 * 신뢰할 수 있는 만세력 기반 간지 매핑 테이블.
 * Claude가 자체 계산을 하지 않고 이 데이터만 사용하도록 강제한다.
 * 기준: 1900년 = 庚子(경자).
 */
export function buildGanjiMappingTable(): string {
  const STEMS = ["甲", "乙", "丙", "丁", "戊", "己", "庚", "辛", "壬", "癸"];
  const BRANCHES = ["子", "丑", "寅", "卯", "辰", "巳", "午", "未", "申", "酉", "戌", "亥"];
  const stemKrList = ["갑", "을", "병", "정", "무", "기", "경", "신", "임", "계"];
  const branchKrList = ["자", "축", "인", "묘", "진", "사", "오", "미", "신", "유", "술", "해"];

  const baseYear = 1900;
  const baseStemIndex = 6; // 庚
  const baseBranchIndex = 0; // 子

  const ganjiTable: Record<number, string> = {};
  for (let year = 1900; year <= 2100; year++) {
    const yearDiff = year - baseYear;
    const stemIndex = (baseStemIndex + yearDiff) % 10;
    const branchIndex = (baseBranchIndex + yearDiff) % 12;
    ganjiTable[year] =
      `${STEMS[stemIndex]}${BRANCHES[branchIndex]}(${stemKrList[stemIndex]}${branchKrList[branchIndex]})`;
  }

  const importantYears = [
    1900, 1998, 2000, 2020, 2022, 2023, 2024, 2025, 2026, 2027, 2028, 2029, 2030, 2050, 2100,
  ];
  let table = "【신뢰할 수 있는 만세력 기반 간지 매핑】\n";
  table += "아래 매핑은 1900년=庚子(경자)를 기준으로 정확히 계산된 데이터입니다.\n";
  table += "단순 질문에 대한 답변: 아래 매핑만 사용하고, 자체 계산은 절대 하지 말 것\n\n";
  for (const year of importantYears) {
    table += `- ${year}년: ${ganjiTable[year]}\n`;
  }
  table += "\n【CRITICAL: 상대적 시간 표현 처리】\n";
  table += "사용자가 '내년', '3년 후', '작년' 등 상대적 시간을 말하면:\n";
  table += "1. 우리가 이미 계산해준 간지만 사용할 것\n";
  table += "2. 절대 자체 계산을 하지 말 것 (예: 辛未年 같은 오류는 신뢰 붕괴)\n";
  table += "3. 상담 맥락에서 제공된 목표 연도의 간지를 반드시 사용할 것\n";
  table += "4. 의심스러우면 위 매핑을 먼저 확인할 것";
  return table;
}

