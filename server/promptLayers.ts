/**
 * 모드에 맞는 L1+L2+L3 고정 계층을 조립한다.
 * @param mode 상담 모드(personal | compatibility)
 * @param opts.splitStyleLayer L3(스타일)를 별도 캐시 블록으로 분리할지 여부.
 *        A/B로 스타일을 자주 바꿀 때만 true 권장(토큰이 캐시 최소단위를 넘을 때 의미 있음).
 */
export function buildSystemLayers(
  mode: ConsultMode,
  opts: { splitStyleLayer?: boolean } = {},
): SystemLayers {
  const l1 = L1_CORE_PERSONA;
  const ganji = buildGanjiMappingTable();
  const l2Policy = mode === "personal" ? L2_PERSONAL_POLICY : L2_COMPATIBILITY_POLICY;
  const l2 = `${ganji}\n${l2Policy}`;
  const l3 = mode === "personal" ? L3_PERSONAL_STYLE : L3_COMPATIBILITY_STYLE;

  if (opts.splitStyleLayer) {
    // 2단 캐시: [L1+L2] (거의 불변) / [L3] (자주 변경)
    return {
      cachedBlocks: [`${l1}\n\n${l2}`, l3],
      versions: LAYER_VERSIONS,
    };
  }

  // 기본: 단일 고정 캐시 블록 [L1+L2+L3]
  return {
    cachedBlocks: [`${l1}\n\n${l2}\n${l3}`],
    versions: LAYER_VERSIONS,
  };
}

// ─────────────────────────────────────────────────────────────
// L4: DYNAMIC — 사주 데이터 포매팅 (RAG/사용자 입력은 호출부에서 결합)
// ─────────────────────────────────────────────────────────────

/** 개인 상담용 사주 데이터 + 대운/시운/플랜 동적 블록 */
export function buildPersonalDynamicContext(saju: SajuResult, plan: string): string {
  // 만세력 엔진의 정식 포맷을 그대로 사용한다.
  // formatSajuForPrompt는 출생연월일+현재일자로 만나이/세는나이를 계산하고,
  // ★현재 대운 구간과 올해 세운을 코드 확정값으로 못박아 주입한다.
  // 또한 조후·기세·12운성·육친·지장간이 모두 포함된 풍부한 데이터를 제공한다.
  const sajuBlock = formatSajuForPrompt(saju);
  // 일주 RAG는 ENABLE_DAY_PILLAR_RAG=true 일 때만 삽입(기본값: OFF)
  const dayGanji = `${saju.pillars.day.stem}${saju.pillars.day.branch}`;
  const dayPillarBlock =
    process.env.ENABLE_DAY_PILLAR_RAG === "true"
      ? buildDayPillarSourceBlock(dayGanji)
      : "";
  const dayPillarPart = dayPillarBlock ? `\n\n${dayPillarBlock}` : "";

  return `## 당신의 사주 정보

${sajuBlock}${dayPillarPart}

## 상담 플랜
- 플랜: ${plan}
- 이 상담은 위 플랜에 맞는 깊이와 시간으로 진행됩니다.`;
}
